#-------------------------------------------------------------------------------
#--- Install Scoop Package Manager ---
#-------------------------------------------------------------------------------
iwr -useb get.scoop.sh | iex

#-------------------------------------------------------------------------------
#--- Install Scoop Apps ---
#-------------------------------------------------------------------------------
scoop bucket add extras
$ScoopApps = @(
    "komorebi",
    "touchcursor"
)

foreach ($app in $ScoopApps) {
    scoop install "$app"
}